export const AI_EVENTS: AIEvent[] = RAW_EVENTS.slice().sort(
  (a, b) => b.year - a.year || (b.month ?? 0) - (a.month ?? 0)
);

export function getGroupedByYear(events: AIEvent[]): YearGroup[] {
  const map = new Map<number, AIEvent[]>();
  for (const event of events) {
    const arr = map.get(event.year) ?? [];
    arr.push(event);
    map.set(event.year, arr);
  }
  return Array.from(map.entries())
    .sort(([a], [b]) => b - a)
    .map(([year, evs]) => ({ year, events: evs }));
}
